    private void LogKayit(String str) {
        try {
            FileOutputStream fOut;
            FileChannel fChan;
            ByteBuffer mBuf;
            fOut = new FileOutputStream("C:/ClientLog.txt");

            //cikti dosyasi icin kanal elde et
            fChan = fOut.getChannel();
            //tampon olustur
            ///System.out.println(str.length());
            mBuf = ByteBuffer.allocateDirect(str.length());
            for (int i = 0; i < str.length(); i++) {
                mBuf.put((byte) (str.charAt(i)));
            }
            // tamponu geri sar
            mBuf.rewind();

            // tamponu cikti dosyasina yaz
            fChan.write(mBuf);

            fChan.close();
            fOut.close();
        } catch (IOException ex) {
            Logger.getLogger(DeskForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

